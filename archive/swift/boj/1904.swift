import Foundation

/*
 [회고: 처음에 어떻게 흐름을 잡았어야 했나]

 1) 문제를 '문자열'로 보기보다 '타일(조각)로 채우기'로 봐야 함
    - 길이 n을 만들 수 있는 조각은 딱 2개뿐: "1"(길이 1), "00"(길이 2)
    - 즉, 어떤 정답도 마지막에는 반드시 "1" 또는 "00"로 끝난다.

 2) 점화식은 '마지막 조각'으로 분해하면 바로 나온다
    - 마지막이 "1"이면: 앞은 길이 (n-1)을 만드는 모든 방법 -> dp[n-1]
    - 마지막이 "00"이면: 앞은 길이 (n-2)을 만드는 모든 방법 -> dp[n-2]
    => dp[n] = dp[n-1] + dp[n-2]

 3) (내가 했던 실수) '짝/홀 보정(minus)' 같은 규칙을 억지로 만들기
    - dp를 세려고 하다 보니 "짝수면 -2, 홀수면 -1" 같은 보정을 넣었는데,
      이건 '어떤 케이스들이 중복/누락되는지'에 대한 근거가 없는 가설이었다.
    - 실제로 n=5에서 바로 깨진다.
      * 잘못된 식: dp[5] = dp[4] + 2*dp[3] - 1 = 10
      * 정답:     dp[5] = dp[4] + dp[3] = 8

 4) 구현 팁: 모듈러는 매 단계 적용해도 정답이 변하지 않는다
    - (a+b)%M == ((a%M)+(b%M))%M 이므로
    - 값이 커져서 Int 오버플로우 나는 것을 막기 위해 루프마다 %MOD 적용

 결론: '마지막 조각 분해'로 점화식을 만들고, 그 점화식만 믿고 구현하면 된다.
 */
func _1904() {
    // Your solution here
    let n = Int(readLine()!)!
    
    var dp: [Int] = .init(repeating: 0, count: 100005)
    dp[1] = 1
    dp[2] = 2
    
    guard n >= 3 else {
        print(dp[n])
        return
    }
    
    for i in 3...n {
        // (잘못된 가정)
        // "00"을 붙이는 경우가 dp[i-2]만큼 있고, 여기에 뭔가가 2배로 늘어나는 것처럼 보였지만,
        // 실제로는 마지막 조각 분해 관점에서 dp[i]는 dp[i-1] + dp[i-2]로 끝난다.
        // 짝/홀에 따라 -2/-1을 빼는 보정(minus)은 중복/누락을 정확히 설명하지 못해서
        // n=5 같은 작은 반례에서 바로 깨진다.
        let minus = i % 2 == 0 ? 2 : 1
        dp[i] = dp[i-1] + 2 * dp[i-2] - minus
    }
    print(dp[n] % 15746)
}

// _1: 정답 풀이 (피보나치 형태 DP)
func _1904_1() {
    // 01타일(BOJ 1904)
    // 길이 n을 만드는 방법은 마지막 조각 기준으로 2가지로만 분해됨
    // 1) ... + "1"  -> 앞은 길이 n-1
    // 2) ... + "00" -> 앞은 길이 n-2
    // 따라서 점화식: dp[n] = dp[n-1] + dp[n-2]

    let MOD = 15746
    let n = Int(readLine()!)!

    guard n >= 3 else {
        // n=1 -> 1, n=2 -> 2 (문제에서 직접 세어보면 바로 나옴)
        print(n)
        return
    }

    // O(1) 메모리로 굴리기
    // prev2 = dp[i-2], prev1 = dp[i-1]
    var prev2 = 1
    var prev1 = 2

    for _ in 3...n {
        let cur = (prev1 + prev2) % MOD
        prev2 = prev1
        prev1 = cur
    }

    print(prev1)
}
